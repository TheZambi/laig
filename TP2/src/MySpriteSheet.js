class MySpriteSheet {
    /**
     * @method constructor
     * @param texture spriteSheet texture
     * @param sizeM spriteSheet length
     * @param sizeN spriteSheet height
     */
    constructor(texture, sizeM, sizeN) {
        this.texture = texture;
        this.sizeM = sizeM;
        this.sizeN = sizeN;
    }
}
